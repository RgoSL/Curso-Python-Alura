# Nono Exercício do Curso de Condicionais

# Variável que Receba o Valor Chave do Programa
Numero = int(input("Digite o número :"))

# Estrutura Condicional
if Numero % 2 == 0: # Operador Aritmético que Retorna o Resto de uma Divisão
    print("Este número é par!")

else:
    print("Este número é ímpar!")