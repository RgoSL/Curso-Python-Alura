# Primeiro Exercício do Curso de Laços

# Eu utilizei o Laço For nesse exemplo porque o objetivo é a unidade que compõe a coleção. Fazendo dele o tipo mais adequado neste contexto.

# Lista de Clientes
Clientes = ["André", "Paulo", "Mariana", "Rafael", "Emilia"] 

# Laço For Para Apresentar Cada um dos Clientes
for Cliente in Clientes: # Cliente é o Responsável Por Apresentar Cada Elemento da Coleção
    print(Cliente)