# Terceiro Exercício do Curso de Laços

# Neste exercício eu optei pelo While, mas o enunciado explicita que é um caso de número fixo. Logo o for seria o mais adequado.

# Variáveis do Programa
Mensagem = "Bem-Vindo ao Buscante!" # Armazena a Mensagem Pedida
Contador = 0 # Armazena o Valor que Será Iterado

# Estrutura de Controle do Tipo Loop
while Contador < 5: # Laço While Para Exibir a Mesma Mensagem Múltiplas Vezes
    print(Mensagem)
    Contador += 1